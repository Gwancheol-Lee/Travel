// QnA Show&Hide
function qnaShowhide1() {
	if (document.getElementById("Qanswer1").style.display == "block") {
		document.getElementById("Qanswer1").style.display = "none";
	} else {
		document.getElementById("Qanswer1").style.display = "block";
	}
}
